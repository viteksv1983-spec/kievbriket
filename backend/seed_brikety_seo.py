    db.commit()
    print(f"Deleted {deleted} existing brikety products.")

    for p in PRODUCTS:
        seo_title = make_seo_title(p["name"])
        seo_desc = make_seo_desc(p["name"])

        product = Product(
            name=p["name"],
            slug=p["slug"],
            price=p["price"],
            image_url=p["image"],
            description=p["desc"],
            is_available=True,
            weight=1.0,
            ingredients=p["ingredients"],
            shelf_life=p["shelf_life"],
            category="brikety",
            stock_quantity=0,
            variants=make_variants(p["price"]),
            # SEO fields
            meta_title=seo_title,
            meta_description=seo_desc,
            meta_keywords="брикети київ, купити брикети київ, паливні брикети доставка київ, брикети для опалення, паливні пелети київ",
            h1_heading=make_h1(p["name"]),
            canonical_url=make_canonical(p["slug"]),
            og_title=seo_title,
            og_description=seo_desc,
            og_image=p["image"],
            schema_json=make_schema(p, seo_desc),
        )
        db.add(product)
        print(f"  [OK] {p['name']} ({p['slug']})")

    db.commit()
    db.close()
    print(f"\n[OK] Seeded {len(PRODUCTS)} brikety products with full SEO data!")


