def seed_drova():
    db = SessionLocal()
    
    # Delete ONLY existing drova products
    deleted = db.query(Product).filter(Product.category == "drova").delete()
    db.commit()
    print(f"Deleted {deleted} existing drova products.")
    
    for p in PRODUCTS:
        seo_title = make_seo_title(p["name"])
        seo_desc = make_seo_desc(p["name"])
        
        product = Product(
            name=p["name"],
            slug=p["slug"],
            price=p["price"],
            image_url=p["image"],
            description=p["desc"],
            is_available=True,
            weight=1.0,
            ingredients=p["ingredients"],
            shelf_life=p["shelf_life"],
            category="drova",
            stock_quantity=0,
            variants=make_variants(p["price"]) if p["price"] > 500 else None,
            # SEO fields
            meta_title=seo_title,
            meta_description=seo_desc,
            meta_keywords="дрова київ, купити дрова, доставка дров, дрова складометр, дрова для опалення",
            h1_heading=make_h1(p["name"]),
            canonical_url=make_canonical(p["slug"]),
            og_title=seo_title,
            og_description=seo_desc,
            og_image=p["image"],
            schema_json=make_schema({**p, "seo_desc": seo_desc}),
        )
        db.add(product)
        print(f"  [OK] {p['name']} ({p['slug']})")
    
    db.commit()
    db.close()
    print(f"\n[OK] Seeded {len(PRODUCTS)} drova products with full SEO data!")


