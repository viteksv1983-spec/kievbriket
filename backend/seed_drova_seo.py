def make_schema(prod):
    schema = {
        "@context": "https://schema.org",
        "@graph": [
            {
                "@type": "Product",
                "name": prod["name"],
                "description": prod["seo_desc"],
                "image": f"{DOMAIN}{prod['image']}",
                "url": f"{DOMAIN}/catalog/drova/{prod['slug']}",
                "brand": {"@type": "Brand", "name": "КиївБрикет"},
                "sku": prod["slug"],
                "aggregateRating": {
                    "@type": "AggregateRating",
                    "ratingValue": "4.9",
                    "reviewCount": "128"
                },
                "offers": {
                    "@type": "Offer",
                    "price": prod["price"],
                    "priceCurrency": "UAH",
                    "availability": "https://schema.org/InStock",
                    "priceValidUntil": "2027-12-31",
                    "seller": {"@type": "Organization", "name": "КиївБрикет"}
                }
            },
            {
                "@type": "FAQPage",
                "mainEntity": [
                    {"@type": "Question", "name": q, "acceptedAnswer": {"@type": "Answer", "text": a}}
                    for q, a in prod["faq"]
                ]
            }
        ]
    }
    return json.dumps(schema, ensure_ascii=False)

