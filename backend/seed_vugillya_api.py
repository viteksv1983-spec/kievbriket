def seed_via_api():
    print(f"Seeding {len(PRODUCTS)} vugillya products via API: {API_BASE}")

    for p in PRODUCTS:
        seo_title = make_seo_title(p["name"])
        seo_desc = make_seo_desc(p["name"])

        payload = {
            "name": p["name"],
            "slug": p["slug"],
            "category": "vugillya",
            "price": p["price"],
            "unit": "т",
            "description": p["desc"],
            "image_url": p["image"],
            "image_alt": f'{p["name"]} — купити у Києві',
            "is_active": True,
            "in_stock": True,
            "weight": 1000.0,
            "ingredients": p.get("ingredients"),
            "shelf_life": p.get("shelf_life"),
            "stock_quantity": 0,
            "variants": [
                {"name": "0.5 тонни", "price": round(p["price"] * 0.5)},
                {"name": "1 тонна", "price": p["price"]},
                {"name": "2 тонни", "price": round(p["price"] * 2 * 0.95)},
            ],
            "faqs_json": [{"q": q, "a": a} for q, a in p["faq"]],
            "meta_title": seo_title,
            "meta_description": seo_desc,
            "meta_keywords": "вугілля київ, купити вугілля київ, антрацит київ, кам'яне вугілля доставка київ",
            "h1_heading": f'{p["name"]} з доставкою по Києву',
            "canonical_url": f"/catalog/vugillya/{p['slug']}",
            "og_title": seo_title,
            "og_description": seo_desc,
            "og_image": p["image"],
            "schema_json": make_schema(p, seo_desc),
        }

        try:
            resp = requests.post(f"{API_BASE}/products/", json=payload, timeout=30)
            if resp.status_code == 200:
                data = resp.json()
                print(f"  [OK] {p['name']} (id={data.get('id')}, slug={p['slug']})")
            else:
                print(f"  [FAIL] {p['name']}: {resp.status_code} — {resp.text[:200]}")
        except Exception as e:
            print(f"  [ERR] {p['name']}: {e}")

    print(f"\nDone! Posted {len(PRODUCTS)} products to {API_BASE}")


if __name__ == "__main__":
    seed_via_api()
