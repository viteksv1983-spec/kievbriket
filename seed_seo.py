def seed_seo():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()
    
    for slug, data in CATEGORY_SEO.items():
        # Update category_metadata explicitly
        cur.execute("""
            UPDATE category_metadata 
            SET seo_h1 = ?, seo_text = ? 
            WHERE slug = ?
        """, (data['seo_h1'], data['seo_text'].strip(), slug))
        
        print(f"Updated SEO for {slug}. Rows affected: {cur.rowcount}")

    conn.commit()
    conn.close()

if __name__ == "__main__":
    seed_seo()
